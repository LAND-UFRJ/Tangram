package Tangram2;

/**
 *
 * @author balves
 */
public class T2MTKMessages {

    private String error;
    private String out;

    public T2MTKMessages() {
        error = "";
        out = "";
    }

    public String getError() {
        return error;
    }

    public String getOut() {
        return out;
    }

    public void setError(String error) {
        this.error = error;
    }

    public void setOut(String out) {
        this.out = out;
    }




}
